document.addEventListener('DOMContentLoaded', () => {

  let container = document.querySelector('.container');
  let background_rect = document.querySelector('.background-rect');
  let blue_rect = document.querySelector('.blue-rect');
  let orange_rect = document.querySelector('.orange-rect');

  container.setAttribute("height", 635);
  container.setAttribute("width", 625);

  background_rect.setAttribute("height", 635);
  background_rect.setAttribute("width", 625);
  background_rect.setAttribute("fill", "#E0DFCA");

  blue_rect.setAttribute("height", 490);
  blue_rect.setAttribute("width", 350);
  blue_rect.setAttribute("fill", "#26ADD6");
  blue_rect.setAttribute("transform", "translate(10, 125) rotate(-15)");

  orange_rect.setAttribute("height", 490);
  orange_rect.setAttribute("width", 350);
  orange_rect.setAttribute("fill", "#F6C996");
  orange_rect.setAttribute("transform", "translate(250, 65) rotate(7.3)");

  const newCirc = document.createElementNS('http://www.w3.org/2000/svg', 'circle');
  container.appendChild(newCirc);
  newCirc.setAttribute("class", "top-left-circ");
  newCirc.setAttribute("height", 100);
  newCirc.setAttribute("width", 100);
  newCirc.setAttribute("fill", "#00000");
});
